                        <p style="text-align:justify"><span style="color:#000000">Until recently, what little the outside world knew of Dolpa was gleaned from artistic and spiritual accounts from early visitors. Peter Matthiessen’s meditative book ‘The Snow Leopard’ and Eric Valli’s stunning movie ‘Himalaya (Caravan),only added to the allure of this unknown land. The region was only opened to foreigners in 1989, and receives a fraction of the visitors thronging other parts of Nepal. With more trekking agencies venturing into Inner Dolpo – allowing access beyond Phoksundo Lake to the 800-year-old Shey Gompa – a truly remarkable natural and cultural experience is there for the taking (even in the monsoon!). Look out for views of mighty Dhaulagiri (8167m), once thought to be the highest mountain in the world.</span></p>

<p style="text-align:justify"><span style="color:#000000">The greener, southern fringes of Dolpa, the largest district in Nepal, are distinctly Hindu. But venture north past the ring of high passes intoarid Inner Dolpo, and you will encounter not only Tibetan Buddhists, but also practitioners of the ancient Bön religion, extant in just two villages. The spirituality of Dolpa is visible everywhere – legend says Dolpa is a Beyul, one of the “hidden valleys” created by Guru Rinpoche as a refuge for those of exceptionally pure mind. Today, its northern reaches are settled by Rokpa farmers and Drokpa nomads from Tibet, who are cut off from the rest of Nepal by snow for most of the year. They live in some of the highest inhabited villages on Earth, nestling amongst mountains of stark, ascetic beauty.</span></p>

<p style="text-align:justify"><span style="color:#000000">In such barren terrain, the spectacle of Nepal’s deepest lake, Phoksundo, is almost beyond describing. Locals believe Phoksundo was formed when a spiteful demonness flooded a village for revealing her whereabouts to the saint Padmasambhava. The surreal sight of the lake, which hosts no aquatic life and appears to fluctuate between a turquoise and ultramarine hue – appears to substantiate the legend. If you follow in the footsteps of generations of nomads, look out for the remains of the ill-fated village below the lake’s surface.</span></p>

