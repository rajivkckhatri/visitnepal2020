                        <p style="text-align:justify">The sight of a white-turbaned Dhami, a shaman, with silver bangles on his arms and gold rings in his ears, will indicate just how far west you are of the modern world. Yet Hinduism and Buddhism blends with animism in local devotions, the most famous object of which was the Khaptad Swami, the renowned ‘eco-saint’ who lived to well over 100 years of age.</p>

<p style="text-align:justify">Organised tourism has so far been limited to the gentle hills and pastures ofthe Swami’s home, Khaptad National Park, with its snowy backdrop of peaks. But the pristine forests host abundant wildlife, including leopards, monkeys, bearsand rich birdlife. For those with a sense of adventure, there is still much to uncover here – exploration that is as much cultural as it is natural. Just one example is the Chamar’s Dasain, when bulls fuelled with rice-wine and marijuana joust for supremacy – the loser becomes dinner and the winner enjoys a year with the cows!</p>

<p style="text-align:justify">Trekking in the Far West is authentic as it gets. Trails wind through a land untouched by the hand of time, past ancient orange and lemon groves and flagstoned medieval villages. The history of the Malla Kingdom that reigned here from the 12th to the 14th century is not readily discernible today. But in a land that history has left behind, the visitor is free to experience a taste of what trekking must have been like when Nepal first opened up to the world beyond its borders.</p>

